// Generate the app's skill-id constants module from the skill registry.
//
// App TypeScript used to name skills by raw string literal — renames and
// deletions silently broke dispatch sites (the deprecated
// meta-research-project alias couldn't be removed for a whole project phase
// because routes/projects.ts named it by string; meta-rename updates
// playbooks/vocab/wikilinks but cannot see TS). Fable review, Finding 4.4.
//
// The generated module (domains/meta/app/server/lib/skill-ids.ts) is
// COMMITTED; two audit checks keep it honest:
//   - skill-ids-module-stale  — file content must equal what this generator
//     would produce from .claude/skills/ right now
//   - app-stale-skill-literal — no whole-string literal in app code may look
//     like a skill id without being a real skill / entry id / archetype
//
// meta-add-skill, meta-rename, and meta-delete run this as part of their
// procedures. Manual: node scripts/generate-skill-ids.mjs
//
// Pure node built-ins — also imported by scripts/audit.mjs for the checks.

import { existsSync, readdirSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = join(__dirname, '..');

export const SKILL_IDS_MODULE_REL = 'domains/meta/app/server/lib/skill-ids.ts';

export function listSkillIds(repoRoot = REPO_ROOT) {
  const dir = join(repoRoot, '.claude', 'skills');
  return readdirSync(dir, { withFileTypes: true })
    .filter((e) => e.isDirectory() && existsSync(join(dir, e.name, 'SKILL.md')))
    .map((e) => e.name)
    .sort();
}

export function constName(id) {
  return id.toUpperCase().replace(/-/g, '_');
}

export function buildSkillIdsSource(ids) {
  const lines = ids.map((id) => `  ${constName(id)}: '${id}',`).join('\n');
  return `// AUTO-GENERATED by scripts/generate-skill-ids.mjs — do not edit by hand.
// Regenerate after adding / renaming / deleting a skill:
//   node scripts/generate-skill-ids.mjs
// The audit check \`skill-ids-module-stale\` ERRORs when this file drifts
// from .claude/skills/. Name skills in app code via SKILL.<NAME> so a
// rename or deletion becomes a compile error instead of a silent stale
// string (see scripts/generate-skill-ids.mjs header for the history).

export const SKILL = {
${lines}
} as const;

export type SkillId = (typeof SKILL)[keyof typeof SKILL];

export const ALL_SKILL_IDS: readonly SkillId[] = Object.values(SKILL);
`;
}

// Whole-string literals that look like skill ids: quote + (dev|meta|research)-…
// + the SAME quote. Prose mentions inside longer strings deliberately don't
// match — the check targets identifier-position literals (tags, maps, keys).
export function extractSkillLikeLiterals(source) {
  const out = [];
  const re = /(['"`])((?:dev|meta|research)-[a-z][a-z0-9-]*)\1/g;
  let m = re.exec(source);
  while (m) {
    out.push(m[2]);
    m = re.exec(source);
  }
  return out;
}

function main() {
  const ids = listSkillIds();
  const target = join(REPO_ROOT, SKILL_IDS_MODULE_REL);
  writeFileSync(target, buildSkillIdsSource(ids));
  console.log(`✓ ${SKILL_IDS_MODULE_REL} regenerated — ${ids.length} skills`);
}

const invokedDirectly =
  process.argv[1] && fileURLToPath(import.meta.url) === process.argv[1];
if (invokedDirectly) {
  try {
    main();
  } catch (e) {
    console.error(e.message);
    process.exit(1);
  }
}
